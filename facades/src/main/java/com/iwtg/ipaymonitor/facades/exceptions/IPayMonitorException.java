package com.iwtg.ipaymonitor.facades.exceptions;

public class IPayMonitorException extends Exception {

	public IPayMonitorException() {
		super();
	}

	public IPayMonitorException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public IPayMonitorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IPayMonitorException(String arg0) {
		super(arg0);
	}

	public IPayMonitorException(Throwable arg0) {
		super(arg0);
	}
	
	

}
